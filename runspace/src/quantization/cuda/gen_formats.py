"""Generate formats.def — the X-macro list of (E, M, IS_SIGNED) for every
qbench_lp format. Mirrors the SIGNED / UNSIGNED comprehensions in __init__.py.

Re-run this script and check in formats.def whenever the format set changes.
"""
import os

E_MAX, M_MAX = 16, 16
W_MIN, W_MAX = 2, 16

formats = []
for e in range(1, E_MAX + 1):
    for m in range(0, M_MAX + 1):
        if W_MIN <= 1 + e + m <= W_MAX:
            formats.append((e, m, 1))
for e in range(1, E_MAX + 1):
    for m in range(0, M_MAX + 1):
        if W_MIN <= e + m <= W_MAX:
            formats.append((e, m, 0))

assert len(formats) == 255, f"expected 255, got {len(formats)}"

out = []
out.append("// Generated by gen_formats.py.  Do not edit by hand.")
out.append("//")
out.append("// X-macro of all qbench_lp formats: F(E, M, IS_SIGNED).  Use:")
out.append("//   #define X(E, M, S)  ...")
out.append("//   LP_FORMAT_LIST(X)")
out.append("//   #undef X")
out.append("")
out.append("#define LP_FORMAT_LIST(F) \\")
for e, m, s in formats:
    out.append(f"    F({e}, {m}, {s}) \\")
out[-1] = out[-1].rstrip(" \\")
out.append("")

target = os.path.join(os.path.dirname(__file__), "formats.def")
with open(target, "w") as f:
    f.write("\n".join(out) + "\n")
print(f"wrote {target} ({len(formats)} entries)")
